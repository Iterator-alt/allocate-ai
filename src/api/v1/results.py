"""Allocation result and chat history API endpoints.

Endpoints:
- GET /runs/{id}/result - Get allocation results for a completed run
- GET /runs/{id}/chat - Get feedback cards / chat history
"""

from decimal import Decimal
from typing import Optional, List

from fastapi import APIRouter, Depends, HTTPException, Request, Query, status
from sqlalchemy.ext.asyncio import AsyncSession

from src.dependencies import get_db
from src.api.schemas import (
    AllocationResultResponse,
    ChannelAllocation,
    KPIProjection,
    ChatHistoryResponse,
    ChatMessage,
    MessageType,
    Severity,
    ErrorResponse,
)
from src.api.middleware import get_session_context, SessionContext
from src.repositories import RunRepository, AllocationResultRepository, ChatHistoryRepository
from src.db.models.run import RunStatus as DBRunStatus

router = APIRouter(prefix="/runs", tags=["results"])


@router.get(
    "/{run_id}/result",
    response_model=AllocationResultResponse,
    responses={
        200: {"description": "Allocation result retrieved"},
        400: {"model": ErrorResponse, "description": "Run not completed"},
        401: {"model": ErrorResponse, "description": "Unauthorized"},
        403: {"model": ErrorResponse, "description": "Forbidden - not your run"},
        404: {"model": ErrorResponse, "description": "Run or result not found"},
    },
)
async def get_allocation_result(
    request: Request,
    run_id: int,
    db: AsyncSession = Depends(get_db),
) -> AllocationResultResponse:
    """Get allocation results for a completed run.

    Returns the budget allocation recommendations generated by the AI,
    including channel-by-channel breakdown, confidence scores, and any
    warnings about data quality.

    The run must be in 'completed' status to retrieve results.
    """
    session: SessionContext = await get_session_context(request)
    run_repo = RunRepository(db)
    result_repo = AllocationResultRepository(db)

    # Get the run
    run = await run_repo.get(run_id)
    if not run:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Run {run_id} not found",
        )

    # Verify the run belongs to this session (or user is owner)
    if run.session_token != session.session_token and not session.is_owner:
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Access denied - this run belongs to a different session",
        )

    # Check run status - results only available when completed
    if run.status != DBRunStatus.COMPLETED.value:
        # Allow failed runs to show partial results
        if run.status == DBRunStatus.FAILED.value:
            result = await result_repo.get_by_run_id(run_id)
            if not result:
                raise HTTPException(
                    status_code=status.HTTP_400_BAD_REQUEST,
                    detail=f"Run failed and no partial results available. Error: {run.error_message}",
                )
        else:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail=f"Results not available - run is in '{run.status}' status. "
                       f"Please wait for completion.",
            )
    else:
        # Get the result for completed run
        result = await result_repo.get_by_run_id(run_id)
        if not result:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail=f"No allocation result found for run {run_id}",
            )

    # Parse allocations from stored JSON
    allocations_data = result.allocations.get("channels", [])
    allocations = [
        ChannelAllocation(
            channel=a.get("name", a.get("channel", "Unknown")),
            share_pct=Decimal(str(a.get("percentage", 0))),
            budget_gross_eur=Decimal(str(a["amount"])) if a.get("amount") else None,
            reasoning=a.get("rationale") or a.get("reasoning"),
        )
        for a in allocations_data
    ]

    # Extract warnings from validation errors if present
    warnings: List[str] = []
    if result.validation_errors:
        issues = result.validation_errors.get("issues", [])
        warnings.extend(
            issue.get("message", "")
            for issue in issues
            if issue.get("severity") == "warning" and issue.get("message")
        )

    # Check if this was served from cache
    is_cached = False
    if run.input_hash:
        # Check if there's an older run with same hash
        cached_run = await run_repo.find_cached_result(run.input_hash)
        if cached_run and cached_run.id != run.id:
            is_cached = True

    return AllocationResultResponse(
        run_id=run.id,
        allocations=allocations,
        total_budget_eur=run.total_budget,
        kpi_projection=None,  # TODO: Add KPI projection from result
        reasoning_summary=result.summary,
        confidence_score=result.confidence_score,
        warnings=warnings,
        is_cached=is_cached,
        created_at=result.created_at,
        updated_at=result.updated_at,
    )


@router.get(
    "/{run_id}/chat",
    response_model=ChatHistoryResponse,
    responses={
        200: {"description": "Chat history retrieved"},
        401: {"model": ErrorResponse, "description": "Unauthorized"},
        403: {"model": ErrorResponse, "description": "Forbidden - not your run"},
        404: {"model": ErrorResponse, "description": "Run not found"},
    },
)
async def get_chat_history(
    request: Request,
    run_id: int,
    message_type: Optional[str] = Query(
        None,
        description="Filter by message type (warning, alert, summary, recommendation, info)",
    ),
    db: AsyncSession = Depends(get_db),
) -> ChatHistoryResponse:
    """Get feedback cards / chat history for a run.

    Returns all feedback messages generated during the allocation process,
    including warnings, alerts, summaries, and recommendations.

    Messages are ordered by display_order for proper frontend rendering.

    Filter by message_type to get only specific types of messages:
    - warning: Data quality notices (yellow cards)
    - alert: Critical issues (red cards)
    - summary: Allocation strategy summary
    - recommendation: Actionable suggestions
    - info: General information
    """
    session: SessionContext = await get_session_context(request)
    run_repo = RunRepository(db)
    chat_repo = ChatHistoryRepository(db)

    # Get the run
    run = await run_repo.get(run_id)
    if not run:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Run {run_id} not found",
        )

    # Verify the run belongs to this session (or user is owner)
    if run.session_token != session.session_token and not session.is_owner:
        raise HTTPException(
            status_code=status.HTTP_403_FORBIDDEN,
            detail="Access denied - this run belongs to a different session",
        )

    # Validate message_type if provided
    valid_types = ["warning", "alert", "summary", "recommendation", "info"]
    if message_type and message_type not in valid_types:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Invalid message_type. Must be one of: {', '.join(valid_types)}",
        )

    # Get chat messages
    messages = await chat_repo.get_by_run_id(run_id, message_type)

    # Convert to response format
    chat_messages = [
        ChatMessage(
            id=msg.id,
            run_id=msg.run_id,
            message_type=MessageType(msg.message_type),
            severity=Severity(msg.severity),
            title=msg.title,
            content=msg.content,
            metadata=msg.extra_data,
            display_order=msg.display_order,
            created_at=msg.created_at,
            updated_at=msg.updated_at,
        )
        for msg in messages
    ]

    # Calculate flags
    has_warnings = any(
        msg.severity in (Severity.WARNING, "warning")
        for msg in chat_messages
    )
    has_alerts = any(
        msg.severity in (Severity.ERROR, "error")
        for msg in chat_messages
    )

    return ChatHistoryResponse(
        run_id=run_id,
        messages=chat_messages,
        total_messages=len(chat_messages),
        has_warnings=has_warnings,
        has_alerts=has_alerts,
    )
